import Foundation

//Tuple to store completion meta data
typealias ActivityCompletion = (activityUUID: String, lastCompletedOn: Date?)


//MARK:- User Progress Model
struct UsersProgressModel: Codable {
    let speakers: Speakers
    let title, dailyReminderText: String
    let activitySkipTargets: [ActivitySkipTarget]
    let modules: [Module]
    let awarenessIconURL, connectionIconURL, insightIconURL, purposeIconURL: String

    enum CodingKeys: String, CodingKey {
        case speakers, title, dailyReminderText, activitySkipTargets, modules
        case awarenessIconURL = "awarenessIconUrl"
        case connectionIconURL = "connectionIconUrl"
        case insightIconURL = "insightIconUrl"
        case purposeIconURL = "purposeIconUrl"
    }
}

struct ActivitySkipTarget: Codable {
    let activityID, buttonTitle, pageTitle: String
    let confirmationPages: [ConfirmationPage]

    enum CodingKeys: String, CodingKey {
        case activityID = "activityId"
        case buttonTitle, pageTitle, confirmationPages
    }
}

struct ConfirmationPage: Codable {
    let centerVertically: Bool
    let content: [JSONAny]
}

struct Module: Codable {
    let title, icon: String
    let contentType: String?
    let parts: [Part]
}

struct Part: Codable {
    let title: String
    let series: [Series]
}

struct Series: Codable {
    let title: String
    let labVisitIcon: String?
    let activities: [Activity]
}

struct Activity: Codable {
    let uuid: String
    let labVisitBackgroundImage: String?
    let tasks: [Task]
    let titleOverride: String?
    let finishedMessage: FinishedMessage?
    let autoStart: Bool?
}

struct FinishedMessage: Codable {
    let headerImage: HeaderImage
    let headerText: String
    let message: [String]
}

struct HeaderImage: Codable {
    let url: String
    let width, height: Int
}

struct Task: Codable {
    let type: PurpleType
    let title: String
    let pages: [JSONAny]?
    let media: TaskMedia?
    let label, description: String?
    let duration: Int?
    let uuid, scoringID, category: String?
    let steps: [JSONAny]?
    let allowBackButton: Bool?
    let practiceOptions: [PracticeOption]?
    let tipOptions: [TipOption]?

    enum CodingKeys: String, CodingKey {
        case type, title, pages, media, label, description, duration, uuid
        case scoringID = "scoringId"
        case category, steps, allowBackButton, practiceOptions, tipOptions
    }
}

struct TaskMedia: Codable {
    let duration: Int
    let speaker: MediaSpeaker
    let mediaURL, mediaPictureURL: MediaMediaPictureURL
    let posture: Posture?

    enum CodingKeys: String, CodingKey {
        case duration, speaker
        case mediaURL = "mediaUrl"
        case mediaPictureURL = "mediaPictureUrl"
        case posture
    }
}

enum MediaMediaPictureURL: String, Codable {
    case empty = ""
}

enum Posture: String, Codable {
    case active = "active"
    case sitting = "sitting"
}

enum MediaSpeaker: String, Codable {
    case abby = "abby"
    case cortland = "cortland"
    case unguided = "unguided"
}

struct PracticeOption: Codable {
    let title, description: String
    let mediaOptions: [TaskMedia]
}

struct TipOption: Codable {
    let speaker: TipOptionSpeaker
    let mediaURL: MediaMediaPictureURL
    let duration: Int
    let title: String
}

enum TipOptionSpeaker: String, Codable {
    case abby = "abby"
    case cortland = "cortland"
}

enum PurpleType: String, Codable {
    case info = "info"
    case learn = "learn"
    case practice = "practice"
    case survey = "survey"
}

struct Speakers: Codable {
    let cortland, abby, unguided: Abby
}

struct Abby: Codable {
    let name, pictureURL: String

    enum CodingKeys: String, CodingKey {
        case name
        case pictureURL = "pictureUrl"
    }
}

